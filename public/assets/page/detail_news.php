        <p><span><strong>1.</strong></span>&nbsp;Giá đất do Ủy ban nhân dân Thành phố quy&nbsp;định, sử dụng làm căn
            cứ để:</p>
        <p><span>a)&nbsp;</span>Tính thuế đối với việc sử dụng đất và chuyển quyền sử dụng đất theo quy định của
            pháp luật;</p>
        <p><span>b)</span>&nbsp;Tính tiền sử dụng đất và tiền thuê đất khi giao đất, cho thuê đất không thông qua
            đấu giá quyền sử dụng đất hoặc đấu thầu dự án có sử dụng đất;</p>
        <p><span>c)&nbsp;</span>Tính giá trị quyền sử dụng đất khi giao đất không thu tiền sử dụng đất cho các tổ
            chức, cá nhân;</p>
        <p><span>d)&nbsp;</span>Xác định giá trị quyền sử dụng đất để tính vào giá trị tài sản của doanh nghiệp nhà
            nước khi doanh nghiệp cổ phần hóa, lựa chọn hình thức giao đất có thu tiền sử dụng đất;</p>
        <p><span>đ)</span>&nbsp;Tính giá trị quyền sử dụng đất để thu lệ phí trước bạ chuyển quyền sử dụng đất theo
            quy định của pháp luật;</p>
        <p><span>e)</span>&nbsp;Tính giá trị quyền sử dụng đất để bồi thường khi Nhà nước thu hồi đất sử dụng vào
            mục đích quốc phòng, an ninh, lợi ích quốc gia, lợi ích công cộng và phát triển kinh tế;</p>
        <p><span>g)</span>&nbsp;Tính tiền bồi thường đối với người có hành vi vi phạm pháp luật về đất đai mà gây
            thiệt hại cho Nhà nước theo quy định của pháp luật.</p>
        <p><span><strong>2.</strong></span>&nbsp;Trường hợp Nhà nước giao đất, cho thuê đất theo hình thức đấu giá
            quyền sử dụng đất, hoặc đấu thầu dự án có sử dụng đất, thì mức giá trúng đấu giá quyền sử dụng đất hoặc
            trúng đấu thầu dự án có sử dụng đất không được thấp hơn mức giá quy định.</p>
        <p><span><strong>3.</strong></span>&nbsp;Quy&nbsp;định này không áp dụng đối với trường hợp người có quyền
            sử dụng đất thỏa thuận về giá đất khi thực hiện các quyền chuyển nhượng, cho thuê, cho thuê lại quyền sử
            dụng đất; góp vốn bằng quyền sử dụng đất.</p>
